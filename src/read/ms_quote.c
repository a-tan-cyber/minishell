#include "../minishell.h"

char	ms_quote_next(char q, char c)
{
	if (c == '\'' && q != '"')
	{
		if (q == '\'')
			return (0);
		return ('\'');
	}
	if (c == '"' && q != '\'')
	{
		if (q == '"')
			return (0);
		return ('"');
	}
	return (q);
}
